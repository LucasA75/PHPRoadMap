<?php

/* 
    Aricmeticos los de siempre
    +	Addition	$x + $y
    -	Subtraction	$x - $y
    *	Multiplication	$x * $y
    /	Division	$x / $y
    %	Modulus	$x % $y
    **	Exponentiation	$x ** $y


    Operadores de asignacion
    x = y	x = y
    x += y	x = x + y	Addition
    x -= y	x = x - y	Subtraction
    x *= y	x = x * y	Multiplication
    x /= y	x = x / y	Division
    x %= y	x = x % y	Modulus
    

    Operadores de Comparacion

    ==	Equal	$x == $y
    ===	Identical	$x === $y	Returns true if $x is equal to $y, and they are of the same type
    !=	Not equal	$x != $y  O tambien    <>	Not equal	$x <> $y
    !==	Not identical	$x !== $y
    >	Greater than	$x > $y
    <	Less than	$x < $y
    >=	Greater than or equal to	$x >= $y
    <=	Less than or equal to	$x <= $y
    
    <=>	Spaceship	$x <=> $y	
    Returns an integer less than, equal to, or greater than zero, 
    depending on if $x is less than, equal to, or greater than $y. Introduced in PHP 7

    

    Incrementables y Decrementables}

    ++$x	Pre-increment	Increments $x by one, then returns $x
    $x++	Post-increment	Returns $x, then increments $x by one
    --$x	Pre-decrement	Decrements $x by one, then returns $x
    --$x	Pre-decrement	Decrements $x by one, then returns $x
    $x--	Post-decrement	Returns $x, then decrements $x by one


    Operadores Logicos

    and	And	$x and $y	True if both $x and $y are true
    or	Or	$x or $y	True if either $x or $y is true
    xor	Xor	$x xor $y	True if either $x or $y is true, but not both
    &&	And	$x && $y	True if both $x and $y are true
    ||	Or	$x || $y	True if either $x or $y is true
    !	Not	!$x	True if $x is not true


    Concadenacion de Strings

    .	Concatenation	$txt1 . $txt2	Concatenation of $txt1 and $txt2

    .=	Concatenation assignment	$txt1 .= $txt2	Appends $txt2 to $txt1



    Asignadores Condicionales


        ?:	 Ternary	$x = expr1 ? expr2 : expr3	
        
        Returns the value of $x.
        The value of $x is expr2 if expr1 = TRUE.
        The value of $x is expr3 if expr1 = FALSE


        ??	 Null coalescing	 $x = expr1 ?? expr2	

        Returns the value of $x.
        The value of $x is expr1 if expr1 exists, and is not NULL.
        If expr1 does not exist, or is NULL, the value of $x is expr2.
        Introduced in PHP 7
















